<h2>Návod na použitie webovej aplikácie</h2>

<div class="col-lg-8">
    
<h4>Prihlásenie do systému</h4>
<p>Do webovej aplikácie sa dá prihlásiť dvoma spôsobmi, cez LDAP (prihlasovacie údaje používané aj do AIS), alebo pomocou
   účtu emailového konta G-mail. Pre použitie konta Google je ale potrebné, aby sa užívateľ prihlásil 
   prvý krát cez účet LDAP a následne pridal svoju emailovú adresu na gmail do svojho konta v systéme.

</p>

<h4>Úvodná stránka</h4>
<p>Po prihlásení do systému sú na hlavnej stránke zobrazené osoby registrované v systéme s príslušným počtom vyučovaní a celkovým množstvom
    prednášok + cvičení, ktoré majú. Po kliknutí na záhlavie tabuľky, je možné zoradiť ju, podľa priezviska osoby alebo celkového počtu prednášky + cvičenia.
    V spodnej časti je možné exportovať túto tabuľku do formátu CSV.

</p>

<h4>Sekcia rozvrhy</h4>
<p>V tejto časti je možné si prezrieť rozvrhy miestností, ústavov, učiteľov, predmetov, dni v týždni
    a skontrolovať, či všetky predmety majú vytvorený rozvrh.

</p>

<h4>Sekcia zobraziť</h4>
<p>Táto sekcia umožňuje prezrieť si všetky miestnosti, osoby, konzultácie, predmety a vyučovacie hodiny
    a následne ich upraviť alebo vymazať, všetky tabuľky sú zoradzovateľné.

</p>

<h4>Sekcia pridať</h4>
<p>Táto sekcia umožňuje pridať miestnosti, osoby, konzultácie, predmety a vyučovacie hodiny.
</p>

<h4>Sekcia akcie</h4>
<p>Táto sekcia umožňuje vygenerovať ICS súbor na importovanie do google calendar, treba zadať dátum konca semestra.
    Tlačidlom export databázy sa exportuje dump databázy do súboru, pri importe sa nahrá tvolený súbor do databázy.
</p>

